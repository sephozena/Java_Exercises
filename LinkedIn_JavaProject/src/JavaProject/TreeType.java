package JavaProject;

/*
 * enum is a special type that represents a group of constants 
 * by using an ENUM, no one can create a new tree (mango, starapple, etc.)
 * the only tree that can exist are those trees listed in the enum class
 * 
 */
public enum TreeType {
	OAK,
	MAPLE,
	PECAN,
	WALLNUT,
	PINE
}
